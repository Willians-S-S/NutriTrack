package com.nutritrack.NutriTrack.entity;

/**
 * Enum que representa os tipos de refeição de um usuário.
 *
 * Valores:
 * <ul>
 *   <li>CAFE_MANHA: Café da manhã</li>
 *   <li>LANCHE: Lanche ou refeição intermediária</li>
 *   <li>ALMOCO: Almoço</li>
 *   <li>JANTAR: Jantar</li>
 *   <li>CEIA: Ceia</li>
 *   <li>POS_TREINO: Pós-treino</li>
 *   <li>OUTRO: Outro tipo de refeição</li>
 * </ul>
 */
public enum TipoRefeicao {
    CAFE_MANHA,
    LANCHE,
    ALMOCO,
    JANTAR,
    CEIA,
    POS_TREINO,
    OUTRO;
}
