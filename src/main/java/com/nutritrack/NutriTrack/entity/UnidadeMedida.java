package com.nutritrack.NutriTrack.entity;

/**
 * Enum que representa as unidades de medida utilizadas nos itens de refeição.
 *
 * Valores:
 * <ul>
 *   <li>GRAMA: Medida em gramas</li>
 *   <li>MILILITRO: Medida em mililitros</li>
 *   <li>UNIDADE: Quantidade por unidade</li>
 *   <li>FATIA: Quantidade em fatias</li>
 *   <li>XICARA: Quantidade em xícaras</li>
 *   <li>COLHER_SOPA: Quantidade em colheres de sopa</li>
 *   <li>PEDACO: Quantidade em pedaços</li>
 * </ul>
 */
public enum UnidadeMedida {
    GRAMA,
    MILILITRO,
    UNIDADE,
    FATIA,
    XICARA,
    COLHER_SOPA,
    PEDACO
}
